export function teamNameKr(teamIdOrTriCode?: string, fallbackName?: string) {
  const raw = String(teamIdOrTriCode ?? "").trim();
  if (!raw) return fallbackName || "팀";

  // 1) NBA teamId (1610...)
  if (TEAM_KR_BY_NBA_ID[raw]) return TEAM_KR_BY_NBA_ID[raw];

  // 2) triCode
  const tri = raw.toUpperCase();
  if (TEAM_KR_BY_TRICODE[tri]) return TEAM_KR_BY_TRICODE[tri];

  // 3) (구) ESPN numeric id
  if (TEAM_KR_BY_ESPN_ID[raw]) return TEAM_KR_BY_ESPN_ID[raw];

  return fallbackName || raw || "팀";
}
